#Operadores Lógicos
# and = Devuelve True si ambos operadores son True ---> a and b
# or = Devuelve True si alguno de los operadores son True ---> a or b
# not = Devuelve True si alguno de los operadores es False ---> not a

a = True
b = False
resultado = a and b
print(f"El resultado es {resultado}")

#Operador or
resultado = a or b
print(f"El resultado es {resultado}")

#Operador not
resultado = not a
print(f"El resultado es {resultado}")
